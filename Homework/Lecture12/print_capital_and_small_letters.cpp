#include<bits/stdc++.h>
using namespace std;
int main()
{
    char count = 'A';
    while (count <= 'Z')
    {
        cout<<count<<" ";
        count++;
    }
    cout<<endl;

    count = 'a';
    while (count <= 'z')
    {
        cout<<count<<" ";
        count++;
    }
    cout<<endl;
    return 0;
}