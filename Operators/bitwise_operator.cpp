#include<bits/stdc++.h>
using namespace std;
int main() {
    
    // & operator
    int ans = 2&3;
    cout<<ans<<endl;
    // | operator
    ans = 2|3;
    cout<<ans<<endl;
    // ^ operator
    ans = 2^3;
    cout<<ans<<endl;
    // << operator
    ans = 2<<3;
    cout<<ans<<endl;
    // >> operator
    ans = 2>>1;
    cout<<ans<<endl;
    // ~ operator
    ans = ~5;
    cout<<ans<<endl;
}