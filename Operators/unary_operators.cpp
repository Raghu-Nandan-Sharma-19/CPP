#include<bits/stdc++.h>
using namespace std;
int main() {
    int num = 10;

    // post increment
    cout<<num++<<endl;
    cout<<num<<endl;
    
    // pre increment
    cout<<++num<<endl;
    cout<<num<<endl;

    // post decrement
    cout<<num--<<endl;
    cout<<num<<endl;

    // pre decrement
    cout<<--num<<endl;
    cout<<num<<endl;

    return 0;
}