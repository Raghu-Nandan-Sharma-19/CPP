#include<bits/stdc++.h>
using namespace std;
int main() {
    // integer
    int x = 10;
    cout<<"x : "<<x<<endl;
    // long
    long x1 = 15;
    cout<<"Long x1 : "<<x1<<endl;
    // long long
    long long x2 = 19000000045;
    cout<<"Long long x2 : "<<x2<<endl;
    // float
    float y = 5.67;
    cout<<"float y : "<<y<<endl;
    // double
    double y1 = 99.19000064783;
    cout<<"Double y1 : "<<y1<<endl;
    // string and get line
    string s;
    getline(cin, s);
    cout<<"Using getline : "<<s<<endl;
    // char
    char ch = 'r';
    cout<<"Character : "<<ch<<endl;
    
    return 0;
}