#include <bits/stdc++.h>
using namespace std;

int main() {
    int int_num = 128;
    cout << int_num << "\n";

    float float_num = 1.234;
    cout << float_num << "\n";

    double double_num = 1.23456;
    cout << double_num << "\n";

    char ch = 'z';
    cout << ch << "\n";

    bool flag = true;
    cout << flag << "\n";

    long long_num = 12345667;
    cout << long_num << "\n";

    long long long_long_num = 1234567890123;
    cout << long_long_num << "\n";

    return 0;
}