#include<bits/stdc++.h>
using namespace std;
int main()
{
    // variable declare
    int a; // integer - 4 bytes
    a = 10; // variable define
    cout<<a<<endl;

    // character - 1 byte
    char c = 'a';
    cout<<c<<endl;

    // float - 4 byte
    float f = 2.31;
    cout<<f<<endl;

    // double - 8 byte
    double d = 3.121;
    cout<<d<<endl;

    // boolean - 1 byte
    bool b = true;
    cout<<b<<endl;
}